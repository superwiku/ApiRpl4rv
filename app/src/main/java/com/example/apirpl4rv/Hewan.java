package com.example.apirpl4rv;

public class Hewan {
    private String family;
    private String jenis;

    public Hewan(String family, String jenis) {
        this.family = family;
        this.jenis = jenis;
    }

    public String getFamily() {
        return family;
    }

    public String getJenis() {
        return jenis;
    }
}
